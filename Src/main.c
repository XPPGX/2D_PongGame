void show_score(int left,int right){
	//BSP_LCD_DisplayChar(10,10,0x30); 
	//the width of LCD is 480 pixel and height is 272
	int right_bound = 240; //where the digits should be displayed the mostly right
	int temp = 0; // temp is used to store the right or left score temporarily
	int score_arr[10]; // stored the each digits of right and left score
	int i = 0; //the var is used to get the length of two scores. ex: len(300) = 3, len(21) = 2
	int flag_in_left = 0; // flag_in_left is used to detect if the left_score > 0 or not
	int flag_in_right = 0; // flag_in_right is used to detect if the right_score > 0 or not
	for(temp = right; temp > 0 ; i = i + 1){
		score_arr[i] = temp % 10;
		temp = temp / 10;
		right_bound = right_bound + 20;
	}
	for(temp = left; temp > 0 ; i = i + 1){
		score_arr[i] = temp % 10;
		temp = temp / 10;
	}
	if(left_score > 0){
		flag_in_left = 1;
	}
	if(right_score > 0){
		flag_in_right = 1;
	}
	if(flag_in_left == 0 && flag_in_right ==0){
		BSP_LCD_DisplayChar(240,10,0x3A);
		BSP_LCD_DisplayChar(220,10,num_arr[0]);
		BSP_LCD_DisplayChar(260,10,num_arr[0]);
	}
	for(int j = 0  ; j < i ; j++){
		BSP_LCD_DisplayChar(240,10,0x3A);
		if(flag_in_left == 0){
			BSP_LCD_DisplayChar(220,10,num_arr[0]);
		}
		if(flag_in_right == 0){
			BSP_LCD_DisplayChar(260,10,num_arr[0]);
		}
		if(right_bound == 240){
			j--;
			right_bound = right_bound - 20;
		}
		else{
			BSP_LCD_DisplayChar(right_bound,10,num_arr[score_arr[j]]);
			right_bound = right_bound - 20;
		}
	}
}

