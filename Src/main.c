void changeSpeed(){
			tmpspeed = speed;
			BSP_TS_GetState(&TS_State);
			if(TS_State.touchDetected == 0){
				while(TS_State.touchDetected == 0){
					if(exe_mode == 1){
						break;
					}
					BSP_TS_GetState(&TS_State);
				}
				x = TS_State.touchX[0];
				y = TS_State.touchY[0];
				if(exe_mode == 0){
					if(x >= 350 && x <= 390&& y >= 110 && y <= 150){
						if(speed == 10){speed = 10;}
						else{speed = speed + 1;}
					}
					else if(x >= 210 && x <= 250 && y >= 110 && y <= 150){
						if(speed == 1){speed = 1;}
						else{speed = speed - 1;}
					}
				}
			}
			if(tmpspeed == speed){}
			else{
				BSP_LCD_SetTextColor(LCD_COLOR_BLACK);
				show_speed(tmpspeed);
				BSP_LCD_SetTextColor(LCD_COLOR_ORANGE);
				show_speed(speed);
			}
}
void check_ini_rand_ball_direction(){
		if(reset_ballmove_random == 0){
			ball_direction = (rand()%4)+1;
			switch(ball_direction){
				case 1:xmove = 1; ymove = 1;break;
				case 2:xmove = 1; ymove = -1;break;
				case 3:xmove = -1; ymove = 1;break;
				case 4:xmove = -1; ymove = -1;break;
			}
			reset_ballmove_random = 1;
		}
}
