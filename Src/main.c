void ball_boomboom(void){
	tmpball_x = ball_x;
	tmpball_y = ball_y;
	check_ini_rand_ball_direction();
	ball_x = ball_x + xmove;
	ball_y = ball_y + ymove;
	if(ball_x == 438){
			if(ball_y >= ry-5 && ball_y < ry + 64){
				ball_collision_flag_H = 1;
				xmove = -1;
			}
		}
		else if(ball_x == 45){
			if(ball_y >= ly-7 && ball_y <= ly + 64){
				ball_collision_flag_H = 0;
				xmove = 1;
			}
		}
	if(ball_x == 470){
			tmpball_x = ball_x;
			tmpball_y = ball_y;
			BSP_LCD_SetTextColor(LCD_COLOR_BLACK);
			BSP_LCD_FillCircle(tmpball_x,tmpball_y,8);
			reset_ballmove_random = 0;
			ball_x = 240;
			ball_y = 136;
			left_score = left_score + 1;
		}
		else if(ball_x == 10){
			tmpball_x = ball_x;
			tmpball_y = ball_y;
			BSP_LCD_SetTextColor(LCD_COLOR_BLACK);
			BSP_LCD_FillCircle(tmpball_x,tmpball_y,8);			
			reset_ballmove_random = 0;
			ball_x = 240;
			ball_y = 136;
			right_score = right_score + 1;
		}
		if(ball_y == 267){
			ball_collision_flag_V = 1;
			ymove = -1;
		}
		else if(ball_y == 5){
			ball_collision_flag_V = 0;
			ymove = 1;
		}
	BSP_LCD_SetTextColor(LCD_COLOR_BLACK);
	BSP_LCD_FillCircle(tmpball_x,tmpball_y,5);
	BSP_LCD_SetTextColor(LCD_COLOR_ORANGE);
	BSP_LCD_FillCircle(ball_x,ball_y,5);
}
